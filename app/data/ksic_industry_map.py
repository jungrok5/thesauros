def ksic_korean_name(code: str | None) -> str | None:
    """Map a KSIC code (3-5 digit) to a Korean industry name.

    Falls back from most-specific to least-specific:
      5-digit → 4-digit → 3-digit → 2-digit → None
    """
    if not code or not str(code).strip().isdigit():
        return None
    c = str(code).strip()
    # Try direct full-code match first (handles 4-5 digit table)
    if c in _LV5:
        return _LV5[c]
    # 3-digit
    if c[:3] in _LV3:
        return _LV3[c[:3]]
    # 2-digit
    if c[:2] in _LV2:
        return _LV2[c[:2]]
    return None
